function profileScores(
  selectionDisplay,
  correctSelections,
  totalScore,
  incorrectSelections,
  week
) {
  var profileScores = document.getElementsByClassName("profile-scores")[0];
  var selectionDisplayJson = JSON.parse(selectionDisplay);
  var tbl = document.createElement("table");
  tbl.style.width = "100px";
  tbl.style.border = "1px solid black";

  var currentTotalScore = document.createElement("h2");
  currentTotalScore.innerHTML =
    "Week " + week.toString() + " Score: " + totalScore;
  profileScores.appendChild(currentTotalScore);

  for (let i = 0; i < 2; i++) {
    const tr = tbl.insertRow();
    for (var team in selectionDisplayJson) {
      const td = tr.insertCell();
      var textColor = "";
      if (correctSelections.toString().split(",").includes(team)) {
        textColor = "green";
      } else if (incorrectSelections.toString().split(",").includes(team)) {
        textColor = "red";
      } else {
        textColor = "black";
      }
      if (i == 0) {
        var cellTeam = document.createElement("img");
        cellTeam.src = teamLogos[team];
        cellTeam.style.width = "40px";
        cellTeam.style.height = "40px";
        td.style.border = "1px solid black";
        cellTeam.innerHTML = team;
        cellTeam.style.color = "black";
        td.appendChild(cellTeam);
      } else {
        var cellScore = document.createElement("b");
        cellScore.innerHTML = selectionDisplayJson[team];
        cellScore.style.color = textColor;
        td.appendChild(cellScore);
      }
      td.style.border = "1px solid black";
    }
  }
  profileScores.appendChild(tbl);
}

