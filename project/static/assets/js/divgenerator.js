function populateWeeklyScores(
  weeklyScoresDict,
  correctSelections,
  incorrectSelections,
  week
) {
  var weeklyScoresDisplay = document.getElementsByClassName("weekly-scores")[0];
  var orderedScores = JSON.parse(weeklyScoresDict);

  for (let i = 0; i < Object.keys(orderedScores).length; i++) {
    var userWeeklyScoresTable = document.createElement("table");
    userWeeklyScoresTable.id = (i + 1).toString();
    userWeeklyScoresTable.style.width = "100px";
    userWeeklyScoresTable.style.border = "1px solid black";
    for (let j = 0; j < 2; j++) {
      const tr = userWeeklyScoresTable.insertRow();
      if (j == 0) {
        const td = tr.insertCell();
        var cell = document.createElement("b");
        cell.innerHTML = orderedScores[i][0];
        cell.style.width = "100px";
        cell.style.height = "40px";
        td.style.border = "3px solid black";
        td.appendChild(cell);
      } else {
        const td = tr.insertCell();
        var cell = document.createElement("b");
        cell.innerHTML = orderedScores[i][1]["score"];
        cell.style.width = "100px";
        cell.style.height = "40px";
        td.style.border = "3px solid black";
        td.appendChild(cell);
      }
      for (
        let k = 0;
        k < Object.keys(orderedScores[i][1]["selections"]).length;
        k++
      ) {
        const td = tr.insertCell();
        if (j == 0) {
          var cell = document.createElement("img");
          cell.src =
            teamLogos[Object.keys(orderedScores[i][1]["selections"])[k]];
          cell.style.width = "40px";
          cell.style.height = "40px";
          td.style.border = "1px solid black";
          td.appendChild(cell);
        } else {
          var cell = document.createElement("b");
          var textColor = "";
          if (
            correctSelections
              .toString()
              .split(",")
              .includes(Object.keys(orderedScores[i][1]["selections"])[k])
          ) {
            textColor = "green";
          } else if (
            incorrectSelections
              .toString()
              .split(",")
              .includes(Object.keys(orderedScores[i][1]["selections"])[k])
          ) {
            textColor = "red";
          } else {
            textColor = "black";
          }
          cell.innerHTML =
            orderedScores[i][1]["selections"][
              Object.keys(orderedScores[i][1]["selections"])[k]
            ];
          td.style.border = "1px solid black";
          td.style.color = textColor;
          td.appendChild(cell);
        }
      }
    }
    weeklyScoresDisplay.appendChild(userWeeklyScoresTable);
  }
}

