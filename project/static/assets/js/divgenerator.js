// Build the Admin page where the 25 games for the week to select are chosen by an admin
function populateAdminPage(games, week, selectedGames) {
  var gameSelection = document.getElementsByClassName("game-selection")[0];
  var gameSelectionList = document.createElement("form");
  gameSelectionList.id = "adminform";

  for (var i = 0; i < games.length; i++) {
    var cb1 = document.createElement("input");
    cb1.type = "checkbox";
    cb1.id = "checkbox" + (i + 1).toString();
    cb1.name = "checkbox" + (i + 1).toString();
    cb1.value = "checkbox" + (i + 1).toString();
    if (
      selectedGames.some(
        (selectedGame) => selectedGame.game_id === games[i].game_id
      )
    ) {
      cb1.checked = true;
    } else {
      cb1.checked = false;
    }

    var cb1label = document.createElement("label");
    cb1label.htmlFor = "checkbox" + (i + 1).toString();
    cb1label.innerHTML = games[i].home + "<b> - VS - </b>" + games[i].away;
    gameSelectionList.appendChild(cb1);
    gameSelectionList.appendChild(cb1label);
    var br = document.createElement("br");
    gameSelectionList.appendChild(br);
  }

  var button = document.createElement("BUTTON");
  button.innerHTML = "Submit Game Selections";
  button.onclick = function () {
    var selections = [];
    for (var i = 0; i < games.length; i++) {
      if (document.getElementById("checkbox" + (i + 1).toString()).checked) {
        selections.push(games[i].game_id);
      }
    }
    var arraylen = selections.length;
    if (arraylen != 25) {
      alert("Please select 25 games, you selected: " + arraylen.toString());
    } else {
      gameSelectionList.setAttribute("method", "post");
      gameSelectionList.setAttribute(
        "action",
        `/selectweeklygames/${week}?selections=${selections}`
      );
    }
  };
  var br = document.createElement("br");
  gameSelectionList.appendChild(br);
  gameSelectionList.appendChild(button);
  gameSelection.appendChild(gameSelectionList);
}

